	function cufon_get_fonts(){
		$cufonfonts = array();
		foreach(glob(THEME_FONT_DIR."/*.js") as $font_file){
			$file_content = file_get_contents($font_file);
			if(preg_match('/font-family":"(.*?)"/i',$file_content,$match)){
				$cufonfonts[$match[1]] = basename($font_file);
			}
		}
		return $cufonfonts;
	}
